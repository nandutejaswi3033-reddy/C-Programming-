#include<stdio.h>
int main()
{
    int a=10;
    float b=3.5;
    double c=50.455;
    char d='T';
    
    printf("integer value:%d\n",a);
    printf("float value:%f\n",b);
    printf("double value:%lf\n",c);
    printf("character value:%c\n",d);
    return 0;
}

/*
output:
integer value:10
float value:3.500000
double value:50.455000
character value:T
*/
